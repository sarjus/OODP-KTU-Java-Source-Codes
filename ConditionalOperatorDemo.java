package com.sjcet.basicPrograms;

public class ConditionalOperatorDemo {
	public static void main(String[] args) {
		 int firstValue = 1;
	     int secondValue = 2;
	     if((firstValue == 1) && (secondValue == 2))
	    	 System.out.println("firstValue is 1 AND secondValue is 2");
	     if((firstValue == 1) || (secondValue == 1))
	            System.out.println("firstValue is 1 OR secondValue is 1");

	}

}
